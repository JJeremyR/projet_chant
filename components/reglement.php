        <h2><b><u>Réglement et conditions du concours :</u></b></h2>
            
        <p>Le concours se déroulera dans l'après-midi et la soirée du samedi suivant,
            quinze jours après la clôture des inscriptions en ligne.</p>
            
        <p>Le candidat devra préalablement s'enregistrer obligatoirement en ligne
            via le formulaire pour pouvoir s'inscrire. Chaque candidat ne peut créer qu'un
            seul compte.</p>
                
        <p>*(En cas d'oubli du mot de passe de la part du candidat,
            une réinitialisation du mot de passe sera possible en contactant le secrétariat
            de l’office de la culture de la mairie de Longuenesse. Un mot de passe par défaut
            sera définis et devra être à nouveau redéfinis par le candidat lors de sa 
            nouvelle connexion au site).</p>
                 
            
        <p>Le candidat devra proposer une chanson en précisant : l'interprète, l’auteur
            et la durée de celle celle-ci.</p>

        <p>(Si un nouveau candidat venais à proposer une chanson déja soumise par un autre 
            candidat enregistré, le nouveau candidat sera amené à choisir une nouvelle chanson. 
            De même si un candidat venait à proposer une reprise dans d'une chanson dans une 
            autre langue, celui-ci devra choisitr à nouveau une chanson pour valider sa 
            participation).</p>
            
            
        <p>Toutes inscription ne sera finalisée qu’aprés réception du paiement soit par 
            chèques via envoie postal à la mairie de Longuenesse (secrétaire du service 
            culturel) ou par chèques/espèces directement auprès de la trésorière de l’office 
            de la culture de la mairie de Longuenesse.</p>

        <p>Une confirmation d'inscription au concours sera envoyée au candidat par mail 
            et sur le site une fois que l'inscription et le paiement auront été validés par 
            l’office de la culture de la mairie de Longuenesse.</p> 

